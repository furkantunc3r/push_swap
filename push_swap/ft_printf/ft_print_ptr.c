#include "ft_printf.h"

int	ft_ptr_len(unsigned long long num)
{
	int	len;

	len = 0;
	while (num != 0)
	{
		num = num / 16;
		len++;
	}
	return (len);
}

void	ft_put_ptr(unsigned long long num)
{
	if (num >= 16)
	{
		ft_put_ptr(num / 16);
		ft_put_ptr(num % 16);
	}
	else
	{
		if (num <= 9)
			ft_putchar_fd((num + '0'), 1);
		else
			ft_putchar_fd((num - 10 + 'a'), 1);
	}
}

int	ft_print_ptr(unsigned long long ptr)
{
	int	ret_len;

	ret_len = 0;
	ret_len += write(1, "0x", 2);
	if (ptr == 0)
		ret_len += write(1, "0", 1);
	else
	{
		ft_put_ptr(ptr);
		ret_len += ft_ptr_len(ptr);
	}
	return (ret_len);
}
