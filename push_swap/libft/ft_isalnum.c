#include "libft.h"

int	ft_isalnum(int chr)
{
	if ((chr >= 'A' && chr <= 'Z') || (chr >= 'a' && chr <= 'z')
		|| (chr <= '9' && chr >= '0'))
		return (1);
	return (0);
}
