#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*new;
	int		i;
	int		len;

	i = 0;
	if (!(s))
		return (NULL);
	len = ft_strlen(s);
	new = (char *)malloc(sizeof(char) * len + 1);
	if (!(new))
		return (NULL);
	while (i < len)
	{
		new[i] = f(i, s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
